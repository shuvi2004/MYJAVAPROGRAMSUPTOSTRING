public class switchcase 
{
    public static void main(String args[])
    {
        int num=30;
        switch(num)
        {
            case 20:System.out.println("case 1");
            break;
            case 10:System.out.println("case 2");
            break;
            case 40:System.out.println("case 3");
            break;
            default:System.out.println("invalid");
        }
    }
}
