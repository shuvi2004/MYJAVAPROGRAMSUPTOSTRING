public class printlnvsprint
 {
    public static void main(String args[])
    {
        for(int i=0;i<4;i++)
        {
            System.out.println("*");  //print in new line.
        }

        for(int j=0;j<4;j++)
        {
            System.out.print("*");  //print in same line.
        }
    }
}
